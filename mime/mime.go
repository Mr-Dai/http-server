package mime

import (
	"strings"
)

var mimeTable = make(map[string]string)

const defaultMime = "application/octet-stream"

// GetMimeByExtension returns the MIME type string of the given file extension.
// Returns `application/octet-stream` if no corresponding MIME type can be found.
func GetMimeByExtension(ext string) (result string) {
	result = mimeTable[ext]
	if result == "" {
		result = defaultMime
	}
	return result
}

// GetMimeByFilename returns the corresponding MIME type string by the extension of
// the given file name. Returns `application/octet-stream` if no corresponding MIME
// type can be found.
func GetMimeByFilename(filename string) string {
	comps := strings.Split(filename, ".")
	if len(comps) < 2 {
		return defaultMime
	}
	return GetMimeByExtension(comps[len(comps)-1])
}

